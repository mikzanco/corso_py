# modulo math
# funzioni built-in min max abd pow
# utilizzare il modulo math sqrt, ceil, floor, pi

x = 3 + 5
y = x + 7

z = ((x + y) * 3 + 35) / x

j = min(4,2,45)
# prende il minimo dei numeri 
print(j)

# abs è absolute e se do a un avlore che è negativo lo rende positivo 

w = max(4,2,45)
# prende il massimo dei numeri 
print(w)

p = pow(4, 3)
# mi fa la potenza, in questo caso è 4 alla terza
print(p)

import math

